    public function all_clients_statment()
    {

        $clients = Client::withSum('paymentRequests', 'total')
            ->withSum('pyments', 'total') // Fixed: was 'amount', should be 'total' as per Pyment model
            ->where('status', 'active')
            ->get();
        // this is the attributes names generated by this query paymentRequests_sum_total , pyments_sum_total

        foreach ($clients as $client) {
            $client->neededAmount = $client->paymentRequests_sum_total - $client->pyments_sum_total;
            if ($client->neededAmount > 0)
                $client->creditStatus =  'debtor';
            elseif ($client->neededAmount < 0)
                $client->creditStatus =  'creditor';
            elseif ($client->neededAmount == 0)
                $client->creditStatus =  'balanced';
        }

        return view('clients_statment', compact('clients', '', ''));
    }


}
