class FinanceFunctionController extends Controller
{

    protected $financeService;
    public function __construct(FinanceService $financeService)
    {
        $this->financeService = $financeService;
    }

    public function finance_report(Request $request)
    {
        $type = $request->get('type');
        $data = $this->financeService->getAll($type);

        return view('finance.report.index', [
            'records' => $data['records'],
            'totals' => $data['totals'],
        ]);

    }

    public function invoice_show_pdf($id)
    {
        $finance = Invoice::findOrFail($id);
        $modelItems = $finance->financeItems;
        $model = 'invoice';
        return view('finance.common.show_pdf', compact('finance', 'modelItems', 'model'));
    }

